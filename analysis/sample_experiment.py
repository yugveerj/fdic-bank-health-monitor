"""A worked A/B analysis on Google's public GA4 e-commerce sample — the method
demonstration for Phase F Stage 2, run end-to-end before any real experiment
exists. No treatment ever ran on this data: the split is a hash of the user id,
the A/A test on it should come up null, and the only "effect" is one we inject
at a known size. What this proves is the pipeline — assignment, SRM, power
math, significance test — on real event data, and writes down what came out.

One aggregate query over bigquery-public-data.ga4_obfuscated_sample_ecommerce
(Nov 2020 – Jan 2021, the full sample); scans a few GB, so run it deliberately.

Usage: uv run python -m analysis.sample_experiment
Writes docs/experiment_sample.md (overwritten on each run).
"""

from __future__ import annotations

import os
import sys
from datetime import date
from pathlib import Path

from dotenv import load_dotenv
from google.cloud import bigquery

from analysis import ab

ALPHA = 0.05
POWER = 0.8
REL_MDE = 0.20  # the smallest relative lift the demo pretends to care about
REL_LIFT = 0.20  # the true effect injected into the treatment arm
SEED = 20260705

DOC_PATH = Path(__file__).resolve().parents[1] / "docs" / "experiment_sample.md"

# FARM_FINGERPRINT makes assignment deterministic and stateless: the same
# user_pseudo_id lands in the same arm on every rerun, no assignment table.
# converters counts users, not events — a user with three purchases is one
# conversion, matching the per-user unit of analysis.
QUERY = """
SELECT
  IF(MOD(ABS(FARM_FINGERPRINT(user_pseudo_id)), 2) = 1, 'treatment', 'control') AS arm,
  COUNT(DISTINCT user_pseudo_id) AS users,
  COUNT(DISTINCT IF(event_name = 'purchase', user_pseudo_id, NULL)) AS converters
FROM `bigquery-public-data.ga4_obfuscated_sample_ecommerce.events_*`
WHERE _TABLE_SUFFIX BETWEEN '20201101' AND '20210131'
GROUP BY arm
"""


def main() -> int:
    load_dotenv()
    project = os.environ.get("GCP_PROJECT", "fdic-monitor")

    client = bigquery.Client(project=project)
    try:
        arms = {r["arm"]: (int(r["users"]), int(r["converters"]))
                for r in client.query_and_wait(QUERY)}
    finally:
        client.close()
    n_c, conv_c = arms["control"]
    n_t, conv_t = arms["treatment"]

    chi2, srm_p = ab.srm_check(n_c, n_t)
    aa_z, aa_p = ab.two_proportion_ztest(conv_c, n_c, conv_t, n_t)

    # both arms are untreated, so the pooled rate is the best baseline estimate
    baseline = (conv_c + conv_t) / (n_c + n_t)
    required = ab.required_n_per_arm(baseline, REL_MDE, alpha=ALPHA, power=POWER)
    powered = min(n_c, n_t) >= required

    conv_t_lifted = ab.apply_synthetic_lift(conv_t, n_t, REL_LIFT, SEED)
    lift_z, lift_p = ab.two_proportion_ztest(conv_c, n_c, conv_t_lifted, n_t)

    report = f"""\
GA4 sample experiment — method demonstration
arms          control {n_c:,} users / {conv_c:,} conv ({conv_c / n_c:.4%}), \
treatment {n_t:,} users / {conv_t:,} conv ({conv_t / n_t:.4%})
SRM           chi2={chi2:.3f} p={srm_p:.4f} -> {"MISMATCH" if srm_p < 0.001 else "split looks fair"}
A/A test      z={aa_z:+.3f} p={aa_p:.4f} -> {"FALSE POSITIVE" if aa_p < ALPHA else "null, as it should be"}
power         baseline {baseline:.4%}, rel MDE {REL_MDE:.0%}: need {required:,}/arm, \
have {min(n_c, n_t):,}/arm -> {"powered" if powered else "underpowered"}
synthetic     +{REL_LIFT:.0%} true lift (seed {SEED}): treatment {conv_t_lifted:,} conv \
({conv_t_lifted / n_t:.4%}), z={lift_z:+.3f} p={lift_p:.2e} -> \
{"detected" if lift_p < ALPHA else "MISSED"}"""
    print(report)

    DOC_PATH.write_text(_doc(
        n_c=n_c, conv_c=conv_c, n_t=n_t, conv_t=conv_t,
        chi2=chi2, srm_p=srm_p, aa_z=aa_z, aa_p=aa_p,
        baseline=baseline, required=required, powered=powered,
        conv_t_lifted=conv_t_lifted, lift_z=lift_z, lift_p=lift_p,
    ))
    print(f"\nwrote {DOC_PATH}")
    return 0


def _doc(*, n_c, conv_c, n_t, conv_t, chi2, srm_p, aa_z, aa_p,
         baseline, required, powered, conv_t_lifted, lift_z, lift_p) -> str:
    return f"""\
# Experiment analysis: a worked demonstration

This is a method demonstration, not a discovery. It runs the full A/B analysis
pipeline — assignment, sample-ratio check, A/A validation, power math, and a
significance test — against Google's public GA4 e-commerce sample
(`bigquery-public-data.ga4_obfuscated_sample_ecommerce`, Nov 1, 2020 –
Jan 31, 2021). No experiment ever ran on that site as far as this split is
concerned; the one "effect" reported below was injected at a known size to
show the pipeline detects what it is powered to detect. The real,
pre-registered experiment on this project's own GA4 traffic is Phase F
Stage 2. Generated by `uv run python -m analysis.sample_experiment` on
{date.today().isoformat()}; the numbers reproduce exactly on rerun.

## Hypothesis framing

The stand-in hypothesis mirrors the shape of a real one: *a treatment raises
the per-user purchase conversion rate by 20% relative*. Unit of analysis is
the user (`user_pseudo_id`), metric is users with at least one `purchase`
event divided by all users in the arm, over the full three-month window.
Counting converting users rather than purchase events keeps the unit of
analysis and the unit of randomization the same, which is what makes the
z-test's independence assumption defensible.

## Assignment mechanism

`MOD(ABS(FARM_FINGERPRINT(user_pseudo_id)), 2)` — odd hash lands in
treatment, even in control. Deterministic hash-mod assignment means no
assignment table to store and no drift between runs: the same user is in the
same arm every time the query executes. In a live experiment the same hash
would gate the actual treatment, so exposure and analysis can never disagree.

## Sample-ratio mismatch check

Run before reading any metric: if the split itself is off, every downstream
number is suspect.

| arm | users | share |
|---|---|---|
| control | {n_c:,} | {n_c / (n_c + n_t):.3%} |
| treatment | {n_t:,} | {n_t / (n_c + n_t):.3%} |

Chi-square goodness of fit against 50/50: chi2 = {chi2:.3f}, p = {srm_p:.4f}.
{"That p is small enough to call a mismatch — in a real experiment this would stop the analysis here."
 if srm_p < 0.001 else
 "No evidence of mismatch; the hash splits this population evenly enough to proceed."}

## A/A test

Neither arm was treated, so the conversion rates should differ only by noise.
This is the pipeline's false-positive check: a significant result here would
mean the machinery manufactures effects out of nothing.

| arm | users | converters | conversion |
|---|---|---|---|
| control | {n_c:,} | {conv_c:,} | {conv_c / n_c:.4%} |
| treatment | {n_t:,} | {conv_t:,} | {conv_t / n_t:.4%} |

Two-proportion z-test: z = {aa_z:+.3f}, p = {aa_p:.4f} —
{"significant, which on untreated data is a false positive and would need investigating."
 if aa_p < ALPHA else "null, as it should be."}

## Power

With the observed baseline and a 20% relative minimum detectable effect
(two-sided alpha {ALPHA}, power {POWER:.0%}):

| quantity | value |
|---|---|
| baseline conversion (pooled) | {baseline:.4%} |
| relative MDE | {REL_MDE:.0%} |
| required users per arm | {required:,} |
| available users per arm | {min(n_c, n_t):,} |
| powered | {"yes" if powered else "no"} |

{"Three months of this sample's traffic is enough to detect a 20% relative lift at the planned error rates."
 if powered else
 "The available traffic does not reach the required sample size; a real test at this MDE would need a longer window or a larger MDE."}

## Synthetic-lift test

A 20% relative lift is injected into the treatment arm with
`apply_synthetic_lift` (seed {SEED}): each non-converter converts with the
probability that yields +20% in expectation, drawn once from a seeded
generator. The true effect size is known by construction, so this asks one
question only — does the pipeline detect an effect of exactly the size the
power calculation promised it could?

| arm | users | converters | conversion |
|---|---|---|---|
| control | {n_c:,} | {conv_c:,} | {conv_c / n_c:.4%} |
| treatment (+{REL_LIFT:.0%} injected) | {n_t:,} | {conv_t_lifted:,} | {conv_t_lifted / n_t:.4%} |

Two-proportion z-test: z = {lift_z:+.3f}, p = {lift_p:.2e} —
{"detected, consistent with the power analysis." if lift_p < ALPHA
 else "not detected, which contradicts the power analysis and would need investigating."}

## Limitations

The detected effect exists because this script put it there; nothing here says
anything about any real treatment. The traffic is Google's obfuscated
demo-store sample, not this project's — its baseline conversion and volume
transfer no conclusions to our dashboard's audience, and its
`traffic_source` fields are user-scoped first-touch attribution, unusable for
session-level segmentation anyway. The A/A test is a single draw, not a
false-positive-rate estimate; it can fail 5% of the time by design. The real
test — pre-registered hypothesis, MDE, and stopping rule written down before
launch — runs on this project's own GA4 export in Phase F Stage 2.
"""


if __name__ == "__main__":
    sys.exit(main())
